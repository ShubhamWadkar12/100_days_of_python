a = "1"
b = "2"
print(a + b)                                      # This will combine the two strings and output "12"

print(int(a) + int(b))                            # This will convert the strings to integers and output 3

c = 1.9
d = 8
print(c+d)                                        # This will add a float and an integer, outputting 9.9

print(int(c) + d)                                 # This will convert the float to an integer (1) and add it to 8, outputting 9
